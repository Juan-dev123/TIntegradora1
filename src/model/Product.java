package model;

public class Product {
	
	String id;
	String name;
	String description;
	int price;
	String nit;
	
	public Product(String id, String name, String description, int price, String nit) {
		this.id = id;
		this.name = name;
		this.description = description;
		this.price = price;
		this.nit = nit;
	}
	
	
}
